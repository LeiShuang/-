public class IntegralBean {
    private String integralUrl;
    private String integralTitle;
    private String integralPrice;
    private String integralNeed;

    public String getIntegralUrl() {
        return integralUrl;
    }

    public void setIntegralUrl(String integralUrl) {
        this.integralUrl = integralUrl;
    }

    public String getIntegralTitle() {
        return integralTitle;
    }

    public void setIntegralTitle(String integralTitle) {
        this.integralTitle = integralTitle;
    }

    public String getIntegralPrice() {
        return integralPrice;
    }

    public void setIntegralPrice(String integralPrice) {
        this.integralPrice = integralPrice;
    }

    public String getIntegralNeed() {
        return integralNeed;
    }

    public void setIntegralNeed(String integralNeed) {
        this.integralNeed = integralNeed;
    }
}
